package Type_Casting;

public class Narrowing_Casting {

	public static void main(String[] args) {
		long l=300;
		int i=(int) l;     //do add to cast
		System.out.println(i);

		double d=39.50;
		float f=(float) d;
		System.out.println(f);
		
		int n=287;
		short s=(short) n;
		System.out.println(s);
	}

}
